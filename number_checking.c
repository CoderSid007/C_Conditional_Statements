#include<stdio.h>

int main()
{
    int n , m;


    printf("enter the number:");
    scanf("%d",&m);


    

    if(m > 0)
    {
        n = 1;
        printf("value of n is %d", n);
    }
    if(m = 0)
    {
        n = 0;
        printf("value of n is %d",n);
    }
    if(m < 0)
    {
        n = -1;
        printf("value of n is %d",n);
    }
   
    return 0;


}